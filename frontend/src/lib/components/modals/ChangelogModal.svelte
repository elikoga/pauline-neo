<h1 class="text-3xl">Neu in Pauline</h1>
<section>
  <h2 class="text-2xl">Konto & Stundenpläne</h2>
  <ul>
    <li>
      <strong>Konten:</strong> Du kannst dich jetzt mit einer E-Mail-Adresse anmelden. Ein
      Anmeldelink wird an deine Adresse geschickt — kein Passwort nötig.
    </li>
    <li>
      <strong>Kalender speichern:</strong> Dein Stundenplan wird auf dem Server gespeichert und
      ist so auf mehreren Geräten verfügbar.
    </li>
    <li>
      <strong>Mehrere Stundenpläne:</strong> Du kannst pro Semester mehrere Stundenpläne anlegen,
      duplizieren und zwischen ihnen wechseln.
    </li>
    <li>
      <strong>Migration:</strong> Bestehende Daten aus dem Browser werden beim ersten Anmelden
      automatisch übertragen — nichts geht verloren.
    </li>
  </ul>
</section>


<section>
  <h2 class="text-2xl">Bedienung</h2>
  <ul>
    <li>
      Rückgängig und Wiederherstellen sind jetzt direkt als Buttons verfügbar und damit auch am Handy
      nutzbar.
    </li>
    <li>Die Aktionsbuttons im Stundenplan passen sich jetzt besser an kleinere Bildschirme an.</li>
  </ul>
</section>
<section>
  <h2 class="text-2xl">Kalender-Export</h2>
  <ul>
    <li>
      Serientermine: Wöchentliche und zweiwöchentliche Veranstaltungen werden als Wiederholungen
      exportiert. Änderungen im Kalenderprogramm müssen dadurch nicht mehr für jeden Einzeltermin
      wiederholt werden.
    </li>
    <li>Mehrere Termine pro Woche bleiben getrennte Serien, zum Beispiel Montag und Dienstag.</li>
    <li>UTF-8-Export: Umlaute und andere Sonderzeichen kommen korrekt im Kalenderprogramm an.</li>
    <li>
      Zeitzone: Der Export enthält Europe/Berlin, damit Termine auch bei Sommer- und
      Winterzeitwechseln zur richtigen Uhrzeit angezeigt werden.
    </li>
    <li>
      Dateiname: Exportierte Dateien enthalten Semester und Exportzeitpunkt, statt immer nur
      <code>pauline-export.ics</code> zu heißen.
    </li>
  </ul>
</section>
